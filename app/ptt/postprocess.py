"""Постобработка распознанного текста: чистка заминок, пунктуация, IT-словарь.

- убирает растянутые междометия-заполнители («э-э-э», «ааа», «ммм», «эм», «кхм»);
- приводит в порядок пробелы, пунктуацию, кавычки, односложные ответы;
- опционально канонизирует IT-термины (исправляет частые искажения распознавания).
"""

from __future__ import annotations

import re

# Заполнители-заминки. Якоримся на границы слов, чтобы не задеть нормальные слова.
_FILLER_RE = re.compile(
    r"\b(?:[эа](?:-?[эа])+|м(?:-?м)+|э+м*|кхм|гм|мда)\b",
    re.IGNORECASE,
)

# Базовый IT-словарь: (паттерн, замена). Регистронезависимо, по границам слов.
# Цель — исправить частые искажения распознавания и канонизировать бренды.
# Двусмысленные слова (редис/Redis, кафка/Kafka) НЕ включаем — испортят бытовую речь.
# Список легко расширяется под свой стек.
_IT_TERMS = [
    # частые искажения распознавания → корректная форма
    (r"\bзайди\s+плей\b", "задеплой"),
    (r"\bзайди\s+плэй\b", "задеплой"),
    (r"\bзаконить\b", "закоммить"),
    (r"\bзаконьте\b", "закоммитьте"),
    # «закоммить» модель коверкает по-разному: закомить/закомите/закамить/заканить
    (r"\bзак[оа]м+ит[еья]?\b", "закоммить"),
    (r"\bзаканит[еья]?\b", "закоммить"),
    (r"\bзапуш\b", "запушь"),
    (r"\bбыкенд", "бэкенд"),
    (r"\bфостап\w*", "FastAPI"),
    (r"\bфастап\w*", "FastAPI"),
    (r"\bpostgress?\b", "PostgreSQL"),
    (r"\bпостгрес\w*", "PostgreSQL"),
    (r"\bgitlaps?\b", "GitLab"),
    (r"\bмедулвари\b", "middleware"),
    (r"\bмиддлвар\w*", "middleware"),
    (r"\bпэйплайн\w*", "pipeline"),
    (r"\bпайплайн\w*", "pipeline"),
    (r"\bкот-ревью\b", "код-ревью"),
    # каноны брендов/аббревиатур
    (r"\bгитлаб\b", "GitLab"),
    # GitHub: гитхаб/гитхап/гит-хаб/гит хаб/выгетхаб/гетхаб
    (r"\b(?:вы)?г[ие]т[\s-]?ха[бп]\b", "GitHub"),
    (r"\bкубернетес\b", "Kubernetes"),
    (r"\bкубер\b", "Kubernetes"),
    (r"\bдокер\b", "Docker"),
    (r"\bпостгрескуэль\b", "PostgreSQL"),
    (r"\bсиай[\s-]?сиди\b", "CI/CD"),
]
_IT_COMPILED = [(re.compile(p, re.IGNORECASE), r) for p, r in _IT_TERMS]


def _apply_it_terms(text: str) -> str:
    for rx, repl in _IT_COMPILED:
        text = rx.sub(repl, text)
    return text


def clean_text(text: str, fillers: bool = True, it_terms: bool = False) -> str:
    """Чистит текст: пунктуация/кавычки всегда; заминки и IT-словарь — по флагам."""
    if not text:
        return text

    if fillers:
        text = _FILLER_RE.sub("", text)
    if it_terms:
        text = _apply_it_terms(text)
    text = re.sub(r"\s{2,}", " ", text)  # схлопнуть двойные пробелы
    text = re.sub(r"\s+([,.!?…:;])", r"\1", text)  # убрать пробел перед пунктуацией
    # схлопнуть осиротевшую пунктуацию после удаления заминки («.,» → «.»)
    text = re.sub(r"([.!?…,:;])(?:\s*[,.!?…:;])+", r"\1", text)

    # пробел после знака препинания, если за ним сразу буква или « (числа не трогаем)
    text = re.sub(r"([,.;:!?…])([«a-zA-Zа-яёА-ЯЁ])", r"\1 \2", text)

    # кавычки-ёлочки: без пробелов внутри, с пробелами снаружи
    text = re.sub(r"«\s+", "«", text)  # «  текст → «текст
    text = re.sub(r"\s+»", "»", text)  # текст  » → текст»
    text = re.sub(r"(\w)«", r"\1 «", text)  # слово«  → слово «
    text = re.sub(r"»(\w)", r"» \1", text)  # »слово  → » слово

    text = re.sub(r"^[\s,.;:!?…\-]+", "", text)  # мусор/пунктуация в начале
    text = text.strip()

    # заглавная в начале и после конца предложения
    text = re.sub(
        r"(^|[.!?…]\s+)([а-яёa-z])",
        lambda m: m.group(1) + m.group(2).upper(),
        text,
    )

    # односложный ответ («Да», «Нет») — убрать финальную точку (но не ? и не !)
    words = text.split()
    if len(words) == 1 and text.endswith(".") and not text.endswith(".."):
        text = text[:-1]
    return text
